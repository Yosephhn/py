from flask import Flask
from hardware.routes import hardware_bp

app = Flask(__name__)
app.register_blueprint(hardware_bp)

if __name__ == '__main__':
    import os
    HOST = os.environ.get('SERVER_HOST', 'localhost')
    try:
        PORT = int(os.environ.get('SERVER_PORT', '5555'))
    except ValueError:
        PORT = 5555
    app.run(HOST, PORT)